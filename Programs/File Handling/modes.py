# modes: File modes are used to specify how a file should be opened, such as for reading, writing, or appending data
# Common File Modes in Python
# 1. "r" (Read Mode):--       file = open("file.txt", "r")
# 2. "w" (Write Mode):--      file = open("file.txt", "w")
# 3. "a" (Append Mode):--     file = open("file.txt", "a")
# 4. "x" (Create Mode):--     file = open("file.txt", "x")
# 5. "t" (Text Mode):--       open("file.txt", "rt")
# 6. "b" (Binary Mode)

# Combination Modes
# Mode	    Meaning
# "r+"	    read + write
# "w+"	    write + read (old data delete)
# "a+"	    append + read
# "rb"	    read binary
# "wb"	    write binary