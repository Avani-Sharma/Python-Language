# defining the lambda function:
# lambda function : it's a function which is used into the python for one line function....
# it is define or initialized using the 'lambda' keyword
# it is an anonymous function which is defined using the lambda keyword
# syntax: lambda parameters: return

# here we have stored the lambda function into the sum_1 variable
