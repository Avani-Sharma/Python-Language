# string indexing

# 1. Print the character at index 0 of the string "python".
a = "python"
print("A: ", a[0])

# 2. Print the last character of "python" using negative indexing.
b = "python"
print("B:", b[-1])

# 3. Print the character at index 4 of "programming".
c = "programming"
print("C:", c[4])

# 4. Print the character at index -2 of "hello".
d = "hello"
print("D:", d[-2])

